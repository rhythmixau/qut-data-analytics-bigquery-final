SELECT
  TRAP_LONGITUDE,
  TRAP_LATITUDE,
  COUNT(DISTINCT STRIKE_AT) AS NUM_KILLS,
  COUNT(DISTINCT TRAP_ID) AS NUM_TRAPS
FROM `qut-data-analytics-capstone`.`goodnature`.`int_trap_details`
WHERE FORMAT_DATE('%Y', CAST(STRIKE_AT AS TIMESTAMP)) = '2020'
AND ACTIVITY_TYPE = 'STRIKE' AND STRIKE_AT IS NOT NULL
GROUP BY TRAP_LONGITUDE, TRAP_LATITUDE
ORDER BY NUM_KILLS DESC, NUM_TRAPS DESC